export function generateRandomBirthdate() {
  const year = Math.floor(Math.random() * (2008 - 1950) + 1950);
  const month = String(Math.floor(Math.random() * 12) + 1).padStart(2, "0");
  const day = String(Math.floor(Math.random() * 28) + 1).padStart(2, "0"); // Simplified
  const birthdate = `${year}-${month}-${day}`;
  return birthdate;
}

