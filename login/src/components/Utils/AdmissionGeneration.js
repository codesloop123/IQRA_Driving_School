export function generateRandomCnic() {
  const firstPart = Math.max(Math.floor(Math.random() * 99999), 10000); // Generate a random 4-digit number (1000-9999)
  const secondPart = Math.max(Math.floor(Math.random() * 9999999), 1000000); // Generate a random 7-digit number
  const thirdPart = Math.min(Math.floor(Math.random() * 9), 9); // Generate a random digit (1-10)

  // Combine parts with hyphens
  const cnic = `${firstPart}-${secondPart}-${thirdPart}`;

  return cnic;
}

